#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*a;
	unsigned char	*b;

	i = 0;
	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	while (i < n)
	{
		if (b[i] != a[i] || b[i] == '\0' || a[i] == '\0')
			return (a[i] - b[i]);
		i++;
	}
	return (0);
}
